package VO;

public class FuncionalidadVO {
	String id;
	String nombre;
	
	public FuncionalidadVO() {
		super();
	}
	public String getId() {
		return id;
	}
	public String getNombre() {
		return nombre;
	}
	public void setId(String id) {
		this.id = id;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

}
