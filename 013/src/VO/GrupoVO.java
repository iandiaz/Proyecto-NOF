package VO;

public class GrupoVO {
	String id;
	String nombre;
	public GrupoVO() {
		super();
		
	}
	public String getId() {
		return id;
	}
	public String getNombre() {
		return nombre;
	}
	public void setId(String id) {
		this.id = id;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
}
